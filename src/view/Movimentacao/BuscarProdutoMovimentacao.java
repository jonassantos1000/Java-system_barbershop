    private void gridMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gridMouseClicked
        if(evt.getClickCount() == 2){
            int codigo= Integer.parseInt(String.valueOf(grid.getValueAt(grid.getSelectedRow(),0)));
            String descricao= String.valueOf(grid.getValueAt(grid.getSelectedRow(),1));
            double valor= Double.valueOf(String.valueOf(grid.getValueAt(grid.getSelectedRow(),2)).replace(",", "."));
            Servico servico = new Servico(codigo,descricao,valor);
            IncluirProdutoMovimentacao incluir;
            incluir = new IncluirProdutoMovimentacao(servico);
            incluir.setVisible(true);
        }
    }//GEN-LAST:event_gridMouseClicked
    
    private static void centralizar(JTable table, int column) {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        table.getColumnModel().getColumn(column).setCellRenderer(centerRenderer);
    }
